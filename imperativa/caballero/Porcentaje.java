package imperativa.caballero;

public class Porcentaje {
    public static void main(String[] args) {
        double numeroReal = 75.0; 
        int porcentaje = 25;
        double resultado = (numeroReal * porcentaje) / 100.0;
        System.out.println(porcentaje + "% de " + numeroReal + " es igual a " + resultado);
    }
}
