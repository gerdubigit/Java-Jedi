package imperativa.iniciado;

public class PotenciaDe2Y3 {
    public static void main(String[] args) {
        double numero = 5;
        
        double potenciaDeDos =  numero * numero;
        double potenciaDeTres =  numero * numero * numero;
        System.out.println("Potencia de 2: " + potenciaDeDos + " - Potencia de 3: " + potenciaDeTres);
    }
}